function corr = RotoDiffPotential(t,drot,eps,al,nn)

if nargin<4 || isempty(al)
    al = 0;
end
if nargin<5 || isempty(nn)
    nn = 10;
end

for n = 1:4 % order of correlator
    N{n} = zeros(nn,2*n+1,n+1,n+1);
    for l = 0:nn-1 % total angular momentum
        for m = -n:n % final z-component of momentum
            for s = 0:n % element of vector of n-th order correlator
                for k = 0:n; % initial z-component of momentum
                    if abs(m)<=l && k<=l
                        g = inline(['WignerRotation(x,' int2str(l) ',' int2str(k) ',' int2str(m) ').^2.*sin(x)'],'x');
                        N{n}(l+1,m+n+1,s+1,k+1) = Rombint('RotoDiffPotentialFun',0,pi,10,l,k,m,n,s,al,eps)/Rombint(g,0,pi,10);
                    else
                        N{n}(l+1,m+n+1,s+1,k+1) = 0;
                    end
                end
            end
        end
    end
end
            
M = RotoDiffMatrix(al);

corr = zeros(length(t),4);
for k=0:4
    for m=-4:4
        A = RotMat(k,m,drot,eps,nn);
        for tt=1:length(t)
            K = expm(t(tt)*A);
            for n = max([1 k abs(m)]):4
                for s = 0:n
                    tmp = K*N{n}(:,m+n+1,s+1,k+1);
                    corr(tt,n) = corr(tt,n) + (1+double(k>0))*(M{n}(:,m+n+1,s+1,k+1)'*tmp(1:n+1));
                end
            end
        end
    end
end


