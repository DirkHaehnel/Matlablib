function y = mClebschGordan(j1,j2,j,m1,m2,m,f)

fac = sqrt(gamma(2*j1+1)*gamma(2*j2+1)/gamma(2*j+1)*gamma(j+m+1)*gamma(j-m+1)/gamma(j1+m1+1)/gamma(j1-m1+1)/gamma(j2+m2+1)/gamma(j2-m2+1));
if f==1
    y = ClebschGordan(j1,j2,j,m1,m2,m)*fac;
else
    y = ClebschGordan(j1,j2,j,m1,m2,m)/fac;
end


