function A = RotMat(k,m,drot,eps,nn)
A = zeros(nn); 
for j=0:nn-1
    if j>abs(m) && j>k
        A(j+1,j) = drot(2)*eps/2*mClebschGordan(j-1,1,j,m,0,m,1)*mClebschGordan(j-1,1,j,k,0,k,-1)*2*(j+1);
        A(j+1,j+1) = drot(2)*eps*mClebschGordan(j,1,j,m,0,m,1)*mClebschGordan(j,1,j,k,0,k,-1);
    end
	A(j+1,j+1) = A(j+1,j+1) - drot(2)*j*(j+1) + diff(drot)*k^2;
    if j>=abs(m) && j>=k && j<nn-1
        A(j+1,j+2) = -drot(2)*eps/2*mClebschGordan(j+1,1,j,m,0,m,1)*mClebschGordan(j+1,1,j,k,0,k,-1)*2*j;
    end
end


